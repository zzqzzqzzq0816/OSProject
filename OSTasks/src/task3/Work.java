package task3;

public class Work {
	private int index;
	private int size;
	public int getIndex() {
		return index;
	}
	public void setIndex(int index) {
		this.index = index;
	}
	public int getSize() {
		return size;
	}
	public Work(int index, int size) {
		this.index = index;
		this.size = size;
	}
	public void setSize(int size) {
		this.size = size;
	}
}
